def _run_cmd(cmd: list[str], cwd: Path | None = None) -> str | None:
    try:
        r = subprocess.run(cmd, capture_output=True, text=True, timeout=5, cwd=cwd)
        return r.stdout.strip() if r.returncode == 0 else None
    except (FileNotFoundError, subprocess.TimeoutExpired):
        return None


def _gh_login_if_needed() -> str | None:
    """Return a GitHub token, offering to run gh auth login if not yet authenticated."""
    token = _run_cmd(["gh", "auth", "token"])
    if token:
        return token

    gh_available = _run_cmd(["gh", "--version"]) is not None
    if not gh_available:
        return None

    console.print("[yellow]gh CLI is installed but not authenticated.[/yellow]")
    if click.confirm("Run gh auth login now?", default=True):
        subprocess.run(["gh", "auth", "login"], check=False)
        return _run_cmd(["gh", "auth", "token"])

    return None


def _detect_azure_env() -> dict[str, str]:
    values: dict[str, str] = {}
    conn_str = os.environ.get("AZURE_STORAGE_CONNECTION_STRING", "")
    if conn_str:
        values["AZURE_STORAGE_CONNECTION_STRING"] = conn_str
    return values


def _detect_github_env(target: Path) -> dict[str, str]:
    values: dict[str, str] = {}
    token = _gh_login_if_needed()
    if token:
        values["GITHUB_TOKEN"] = token
    remote_url = _run_cmd(["git", "remote", "get-url", "origin"], cwd=target)
    if remote_url:
        m = re.search(r"github\.com[:/]([^/]+)/([^/]+?)(?:\.git)?$", remote_url)
        if m:
            values["GITHUB_OWNER"] = m.group(1)
            values["GITHUB_RUNS_REPO"] = m.group(2)
    return values


def _read_env_file(env_file: Path) -> dict[str, str]:
    existing: dict[str, str] = {}
    if not env_file.exists():
        return existing
    for line in env_file.read_text().splitlines():
        line = line.strip()
        if line and not line.startswith("#") and "=" in line:
            k, _, v = line.partition("=")
            existing[k.strip()] = v.strip()
    return existing


_SECRET_KEYS = {"GITHUB_TOKEN", "AZURE_STORAGE_CONNECTION_STRING"}
_BACKEND_KEYS: dict[str, list[str]] = {
    "azure": ["AZURE_STORAGE_CONNECTION_STRING"],
    "github": ["GITHUB_TOKEN", "GITHUB_OWNER", "GITHUB_RUNS_REPO"],
}


def _write_env(target: Path, backend: str, force: bool) -> None:
    env_file = target / ".env"
    existing = _read_env_file(env_file)
    detected = _detect_azure_env() if backend == "azure" else _detect_github_env(target)

    KEYS = _BACKEND_KEYS.get(backend, _BACKEND_KEYS["github"])
    missing = [k for k in KEYS if k not in existing]

    if not missing and not force:
        console.print(f"[dim]skip[/dim] .env (all values already present)")
        return

    keys_to_set = KEYS if force else missing
    console.print(f"\n[bold]Environment setup[/bold] ({'updating' if existing else 'creating'} .env)")

    new_values: dict[str, str] = {}
    for key in keys_to_set:
        if key in detected:
            new_values[key] = detected[key]
            display = detected[key][:8] + "…" if key in _SECRET_KEYS else detected[key]
            console.print(f"  [dim]auto-detected[/dim] {key}={display}")
        else:
            value = click.prompt(key, default="", hide_input=key in _SECRET_KEYS, show_default=False, prompt_suffix=": ")
            new_values[key] = value

    # Strip previous harness section, keep everything else intact
    raw = ""
    if env_file.exists():
        raw_lines = env_file.read_text().splitlines(keepends=True)
        harness_start = next(
            (i for i, l in enumerate(raw_lines) if l.strip() == "# Generated by agentharness init"),
            None,
        )
        raw = "".join(raw_lines[:harness_start] if harness_start is not None else raw_lines)

    all_harness = {**{k: existing[k] for k in KEYS if k in existing}, **new_values}
    harness_section = "# Generated by agentharness init\n" + "".join(
        f"{k}={v}\n" for k, v in all_harness.items() if v
    )
    separator = "\n" if raw and not raw.endswith("\n\n") else ""
    env_file.write_text(raw + separator + harness_section)
    action = "updated" if existing else "wrote"
    console.print(f"[green]{action}[/green] .env")


def _print_started(feature_id: str) -> None:
    """Print pipeline start confirmation."""
    console.print(f"[green]Pipeline started:[/green] {feature_id}")
    console.print(f"Monitor: [bold]agentharness watch[/bold]")


async def _implement_with_epic_check(feature_id: str, config) -> None:
    """Check if feature is an epic child with paused parent before enqueuing."""
    # GitHub backend only
    if config.storage_backend != "github":
        await enqueue_planner(feature_id, config)
        _print_started(feature_id)
        return

    state_mgr = create_state_manager(config)
    gh_client = GitHubClient.from_config(config)

    try:
        # Try to load existing state
        try:
            state = await state_mgr.get(feature_id)
        except KeyError:
            # Raw issue with no state yet — enqueue_planner handles preconditions
            await enqueue_planner(feature_id, config)
            _print_started(feature_id)
            return

        # Check if this is an epic child
        if state.epic_parent is not None:
            try:
                parent_issue = await gh_client.get_issue(state.epic_parent)
            except GitHubApiError as exc:
                console.print(f"[yellow]Warning:[/yellow] Could not check parent epic #{state.epic_parent}: {exc.message}")
                console.print("Proceeding anyway — run agentharness status to check epic state.")
                # Don't block — proceed to enqueue_planner
            else:
                parent_labels = [label["name"] for label in parent_issue.get("labels", [])]

                if EPIC_PAUSED in parent_labels:
                    console.print("[red]Epic is paused[/red] — a child feature has failed.")
                    console.print(f"Parent epic: #{state.epic_parent}")
                    console.print(
                        "To retry: fix the failing child's brief, remove the epic:paused label from "
                        f"#{state.epic_parent}, then re-run this command."
                    )
                    raise SystemExit(1)

        # Non-epic or parent is not paused — proceed
        await enqueue_planner(feature_id, config)
        _print_started(feature_id)

    finally:
        await gh_client.close()
        await state_mgr.close()


@click.group()
@click.option("--config", "config_path", default=None, type=click.Path(), help="Path to config.json")
@click.pass_context
def main(ctx: click.Context, config_path: str | None) -> None:
    """AgentHarness — autonomous agentic development pipeline."""
    ctx.ensure_object(dict)
    ctx.obj["config_path"] = Path(config_path) if config_path else None


@main.command()
@click.pass_context
def brainstorm(ctx: click.Context) -> None:
    """Start an interactive brainstorm session to define a feature brief."""
    config_path = ctx.obj.get("config_path")
    try:
        config = load_config(config_path) if config_path else None
    except FileNotFoundError:
        config = None  # Config not required until submission

    from agentharness.brainstorm import start_brainstorm
    start_brainstorm(config)


