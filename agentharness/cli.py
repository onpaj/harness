@main.command("convert")
@click.argument("issue_number", type=int)
@click.pass_context
def convert(ctx: click.Context, issue_number: int) -> None:
    """Convert an existing GitHub issue into an AgentHarness feature (in-place).

    Detects epic sub-issue relationships automatically and creates branches accordingly.
    Equivalent to the /convertforagent skill but as a CLI command.
    """
    config = load_config(ctx.obj.get("config_path"))

    async def run() -> None:
        gh_client = GitHubClient.from_config(config)
        try:
            issue = await gh_client.get_issue(issue_number)
        except GitHubApiError as exc:
            console.print(f"[red]Error fetching issue #{issue_number}:[/red] {exc.message}")
            raise SystemExit(1) from exc
        finally:
            await gh_client.close()

        title = issue.get("title") or "untitled"
        feature_id = f"feat-{slug_title(title)}"
        console.print(f"[dim]Issue #{issue_number}:[/dim] {title}")
        console.print(f"[dim]Feature ID:[/dim] {feature_id}")
        await _convert_raw_issue(feature_id, config)
        console.print(f"[green]Converted:[/green] {feature_id}")
        console.print(f"Start pipeline: [bold]agentharness implement {feature_id}[/bold]")

    asyncio.run(run())


@main.command("observe")
@click.option("--auto", is_flag=True, help="Enable auto-mode: drain brainstormed features serially (one at a time).")
@click.pass_context
def observe(ctx: click.Context, auto: bool) -> None:
    """Single observer process: polls all queues, spawns a subprocess per task."""
    import subprocess
    config = load_config(ctx.obj.get("config_path"))
    exe = str(Path(sys.executable).parent / "agentharness")
    log_dir = Path("logs")
    log_dir.mkdir(exist_ok=True)
    log_file = log_dir / "observer.log"
    fh = open(log_file, "a")
    cmd = [exe, "_observe"]
    if auto:
        cmd.append("--auto")
    proc = subprocess.Popen(
        cmd,
        stdout=fh,
        stderr=fh,
        start_new_session=True,
    )
    console.print(f"[green]Observer started[/green] (pid {proc.pid}) → logs/observer.log")
    if auto:
        console.print("[dim]Auto-mode: ON — will drain brainstormed features serially.[/dim]")
    console.print("Monitor with: [bold]agentharness watch[/bold]")


@main.command("_observe")
@click.option("--auto", is_flag=True, hidden=True)
@click.pass_context
def _observe(ctx: click.Context, auto: bool) -> None:
    """Internal: run the observer loop (do not call directly)."""
    from agentharness import auto_mode
    from agentharness.observer import observe as _run_observe
    from agentharness.run_task import configure_logging
    configure_logging()
    config = load_config(ctx.obj.get("config_path"))
    if auto:
        auto_mode.enable()
    asyncio.run(_run_observe(config))


