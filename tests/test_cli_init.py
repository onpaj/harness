"""Tests for backend-aware init: _write_env, _detect_azure_env, and init_project backend selection."""

from __future__ import annotations

import json
import os
from pathlib import Path
from unittest.mock import MagicMock, patch

import pytest
from click.testing import CliRunner

from agentharness.cli import _detect_azure_env, _read_env_file, _write_env, main


# ---------------------------------------------------------------------------
# _detect_azure_env
# ---------------------------------------------------------------------------


def test_detect_azure_env_returns_connection_string_from_environment() -> None:
    with patch.dict(os.environ, {"AZURE_STORAGE_CONNECTION_STRING": "DefaultEndpointsProtocol=https;test"}):
        result = _detect_azure_env()
    assert result == {"AZURE_STORAGE_CONNECTION_STRING": "DefaultEndpointsProtocol=https;test"}


def test_detect_azure_env_returns_empty_when_not_set() -> None:
    env = {k: v for k, v in os.environ.items() if k != "AZURE_STORAGE_CONNECTION_STRING"}
    with patch.dict(os.environ, env, clear=True):
        result = _detect_azure_env()
    assert result == {}


# ---------------------------------------------------------------------------
# _write_env — azure backend
# ---------------------------------------------------------------------------


def test_write_env_azure_creates_env_with_connection_string(tmp_path: Path) -> None:
    with patch("agentharness.cli._detect_azure_env", return_value={"AZURE_STORAGE_CONNECTION_STRING": "conn-str-val"}):
        _write_env(tmp_path, backend="azure", force=False)

    env_file = tmp_path / ".env"
    assert env_file.exists()
    content = env_file.read_text()
    assert "AZURE_STORAGE_CONNECTION_STRING=conn-str-val" in content
    assert "GITHUB_TOKEN" not in content


def test_write_env_azure_prompts_when_connection_string_missing(tmp_path: Path) -> None:
    with patch("agentharness.cli._detect_azure_env", return_value={}):
        with patch("click.prompt", return_value="entered-conn-str") as mock_prompt:
            _write_env(tmp_path, backend="azure", force=False)

    mock_prompt.assert_called_once()
    call_kwargs = mock_prompt.call_args
    assert call_kwargs.kwargs.get("hide_input") is True

    env_file = tmp_path / ".env"
    assert "AZURE_STORAGE_CONNECTION_STRING=entered-conn-str" in env_file.read_text()


def test_write_env_azure_skips_when_already_present(tmp_path: Path) -> None:
    env_file = tmp_path / ".env"
    env_file.write_text("# Generated by agentharness init\nAZURE_STORAGE_CONNECTION_STRING=existing\n")

    with patch("agentharness.cli._detect_azure_env", return_value={}) as mock_detect:
        with patch("click.prompt") as mock_prompt:
            _write_env(tmp_path, backend="azure", force=False)

    mock_prompt.assert_not_called()
    assert "AZURE_STORAGE_CONNECTION_STRING=existing" in env_file.read_text()


def test_write_env_azure_force_overwrites_existing(tmp_path: Path) -> None:
    env_file = tmp_path / ".env"
    env_file.write_text("# Generated by agentharness init\nAZURE_STORAGE_CONNECTION_STRING=old-value\n")

    with patch("agentharness.cli._detect_azure_env", return_value={"AZURE_STORAGE_CONNECTION_STRING": "new-value"}):
        _write_env(tmp_path, backend="azure", force=True)

    assert "AZURE_STORAGE_CONNECTION_STRING=new-value" in env_file.read_text()


def test_write_env_azure_preserves_user_content_before_harness_section(tmp_path: Path) -> None:
    env_file = tmp_path / ".env"
    env_file.write_text("USER_KEY=user-value\n# Generated by agentharness init\nAZURE_STORAGE_CONNECTION_STRING=old\n")

    with patch("agentharness.cli._detect_azure_env", return_value={"AZURE_STORAGE_CONNECTION_STRING": "new-conn"}):
        _write_env(tmp_path, backend="azure", force=True)

    content = env_file.read_text()
    assert "USER_KEY=user-value" in content
    assert "AZURE_STORAGE_CONNECTION_STRING=new-conn" in content


