def test_init_project_defaults_to_existing_backend_in_config(tmp_path: Path) -> None:
    pipeline_dir = tmp_path / ".pipeline"
    pipeline_dir.mkdir()
    config_file = pipeline_dir / "config.json"
    config_file.write_text(json.dumps({"storage_backend": "github"}))

    prompts: list[str] = []

    def capture_prompt(text, **kwargs):
        prompts.append(text)
        return kwargs.get("default", "azure")

    runner = CliRunner()
    with patch("agentharness.cli._write_env"):
        with patch("click.prompt", side_effect=capture_prompt):
            runner.invoke(main, ["--config", str(config_file), "init", "--dir", str(tmp_path)])

    # The backend prompt should have defaulted to "github" (existing config value)
    backend_call = next((c for c in prompts if "backend" in c.lower() or "storage" in c.lower()), None)
    assert backend_call is not None


def test_init_project_defaults_to_azure_when_no_existing_config(tmp_path: Path) -> None:
    defaults: list[str] = []

    def capture_prompt(text, **kwargs):
        if "backend" in text.lower() or "storage" in text.lower():
            defaults.append(str(kwargs.get("default", "")))
        return kwargs.get("default", "azure")

    runner = CliRunner()
    with patch("agentharness.cli._write_env"):
        with patch("click.prompt", side_effect=capture_prompt):
            runner.invoke(main, ["init", "--dir", str(tmp_path)])

    assert "azure" in defaults


def test_init_project_passes_selected_backend_to_write_env(tmp_path: Path) -> None:
    pipeline_dir = tmp_path / ".pipeline"
    pipeline_dir.mkdir()
    config_file = pipeline_dir / "config.json"
    config_file.write_text(json.dumps({"storage_backend": "github"}))

    write_env_calls: list[tuple] = []

    def capture_write_env(target, backend, force):
        write_env_calls.append((target, backend, force))

    runner = CliRunner()
    with patch("agentharness.cli._write_env", side_effect=capture_write_env):
        with patch("click.prompt", return_value="azure"):
            runner.invoke(main, ["--config", str(config_file), "init", "--dir", str(tmp_path)])

    assert len(write_env_calls) == 1
    _, called_backend, _ = write_env_calls[0]
    assert called_backend == "azure"
